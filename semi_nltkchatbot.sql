-- ဒေတာဘေ့စ်ကို တည်ဆောက်ပါ (ရှိပြီးသားမဟုတ်ရင်)
CREATE DATABASE IF NOT EXISTS semi_nltkchatbot CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;

-- အသုံးပြုရန် ဒေတာဘေ့စ်ကို ရွေးချယ်ပါ
USE semi_nltkchatbot;

-- ရှိပြီးသား table များကို ဖျက်ပစ်ပါ (အသစ်ပြန်ဆောက်လိုပါက)
DROP TABLE IF EXISTS answers;
DROP TABLE IF EXISTS questions;
DROP TABLE IF EXISTS keywords;
DROP TABLE IF EXISTS unanswered_queries; -- unanswered_queries table ကိုလည်း ဖျက်ပစ်ပါ

-- keywords table ကို တည်ဆောက်ပါ
CREATE TABLE keywords (
    id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    keyword VARCHAR(255) NOT NULL UNIQUE
);

-- questions table ကို တည်ဆောက်ပါ
CREATE TABLE questions (
    id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    keyword_id BIGINT UNSIGNED NOT NULL,
    question_text TEXT NOT NULL,
    FOREIGN KEY (keyword_id) REFERENCES keywords(id) ON DELETE CASCADE
);

-- answers table ကို တည်ဆောက်ပါ
CREATE TABLE answers (
    id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    question_id BIGINT UNSIGNED NOT NULL,
    answer_text LONGTEXT NOT NULL,
    FOREIGN KEY (question_id) REFERENCES questions(id) ON DELETE CASCADE
);

-- unanswered_queries table ကို တည်ဆောက်ပါ (အဖြေမပေးနိုင်သော မေးခွန်းများကို မှတ်တမ်းတင်ရန်)
CREATE TABLE unanswered_queries (
    id BIGINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    query_text TEXT NOT NULL,
    timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

